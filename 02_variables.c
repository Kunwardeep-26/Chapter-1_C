#include <stdio.h>

int main()
{
    // variable type 1
    int a = 4;
    // variable type 2
    float b = 8.5;
    // variable type 3
    char c = '#';
    // variable d
    int d = 45;
    // mix variable
    int e = 45 + 9;
    // showing the output
    printf("the value of b is %f \n ", b);
    printf("the value of a is %d \n", a);
    printf("the value of c is %c \n", c);
    // adding two variable
    printf("sum of a and d is %d \n", a + d);
    // printing a mix variable
    printf("the value of e is %d", e);

    return 0;
}